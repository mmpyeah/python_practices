# 字符串拼接的几种方式

# ============================================================
# 方式1：+ 运算符拼接
# 优点：简单直观
# 缺点：每次拼接都会创建新字符串，大量拼接时性能较差
# ============================================================
def function1():
    a = '你好'
    b = '世界'
    result = a + b
    print("+ 拼接：", result)          # 你好世界
    print("+ 拼接（含空格）：", a + ' ' + b)  # 你好 世界


# ============================================================
# 方式2：% 格式化字符串（旧式风格）
# 常用占位符：
#   %s  → 字符串 (string)
#   %d  → 整数   (decimal integer)
#   %f  → 浮点数 (float)，默认保留6位小数
#   %g  → 自动选择 %f 或 %e（科学计数法）
#   %%  → 输出字面量 %
# 注意：没有 %h、%i 这两个占位符
# ============================================================
def function2():
    a = '你好'
    b = '世界哈哈'
    c = 1024
    d = 3.14159
    print("%s%s" % (a, b))              # 你好世界哈哈
    print("%s%s%d" % (a, b, c))         # 你好世界哈哈1024
    print("整数：%d，浮点数：%.2f" % (c, d))  # 整数：1024，浮点数：3.14
    print("百分比：%.1f%%" % (87.5,))   # 百分比：87.5%


# ============================================================
# 方式3：str.format() 方法
# 优点：可读性好，支持位置参数和关键字参数
# ============================================================
def function3():
    a = 3
    b = 'Python'

    # 按位置填充
    print("数字是 {}，语言是 {}".format(a, b))

    # 按索引填充
    print("{0} 很有趣，{1} + {1} = {0}".format(a * 2, b))

    # 按关键字填充
    print("{lang} 版本 {ver}".format(lang=b, ver=a))

    # 格式控制：宽度、对齐、小数位
    pi = 3.14159
    print("pi = {:.2f}".format(pi))          # 保留2位小数
    print("{:>10}".format("右对齐"))          # 右对齐，宽度10
    print("{:<10}".format("左对齐"))          # 左对齐，宽度10
    print("{:^10}".format("居中"))            # 居中，宽度10


# ============================================================
# 方式4：f-string（推荐，Python 3.6+）
# 优点：最简洁、可读性最强、性能最好
# ============================================================
def function4():
    name = 'Python'
    version = 3.12
    score = 98.567

    print(f"语言：{name}，版本：{version}")
    print(f"成绩：{score:.1f} 分")           # 保留1位小数
    print(f"1 + 1 = {1 + 1}")               # 表达式直接嵌入
    print(f"{name!r}")                       # !r 调用 repr()
    print(f"{name.lower()} 转小写")          # 方法调用


# ============================================================
# 方式5：join() 方法
# 优点：拼接大量字符串时性能最优（推荐在循环中使用）
# ============================================================
def function5():
    words = ['你好', '世界', 'Python']

    result1 = ''.join(words)          # 无分隔符
    result2 = '-'.join(words)         # 用 - 连接
    result3 = ', '.join(words)        # 用逗号+空格连接

    print("无分隔符：", result1)      # 你好世界Python
    print("- 连接：", result2)        # 你好-世界-Python
    print(", 连接：", result3)        # 你好, 世界, Python

    # 与 + 的性能对比说明（循环中推荐 join）
    parts = []
    for i in range(5):
        parts.append(f"第{i}项")
    print("join 循环拼接：", '、'.join(parts))


# ============================================================
# 主程序：依次演示所有方式
# ============================================================
if __name__ == '__main__':
    print("=" * 40)
    print("方式1：+ 运算符")
    function1()

    print("=" * 40)
    print("方式2：% 格式化")
    function2()

    print("=" * 40)
    print("方式3：str.format()")
    function3()

    print("=" * 40)
    print("方式4：f-string（推荐）")
    function4()

    print("=" * 40)
    print("方式5：join()")
    function5()
